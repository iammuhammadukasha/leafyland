/**
 * One-shot Supabase setup for LeafyLand.
 *
 * Usage (from repo root):
 *   $env:SUPABASE_DB_PASSWORD="your-db-password"
 *   $env:SUPABASE_SERVICE_ROLE_KEY="eyJ..."
 *   $env:SUPABASE_ANON_KEY="eyJ..."
 *   node scripts/complete-supabase-setup.mjs
 *
 * Optional:
 *   $env:SUPABASE_ACCESS_TOKEN="sbp_..."  # disables email confirm via Management API
 */
import { execSync } from 'node:child_process';
import { createRequire } from 'node:module';
import { readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const require = createRequire(import.meta.url);
const pg = require(join(dirname(fileURLToPath(import.meta.url)), '..', 'server', 'node_modules', 'pg'));

const PROJECT_REF = 'csvvtxmhmkfppvahmghf';
const SUPABASE_URL = `https://${PROJECT_REF}.supabase.co`;
const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const SERVER_ENV = join(ROOT, 'server', '.env');
const CLIENT_ENV = join(ROOT, 'client', '.env');

const dbPassword = process.env.SUPABASE_DB_PASSWORD;
const serviceRoleKey = process.env.SUPABASE_SERVICE_ROLE_KEY;
const anonKey = process.env.SUPABASE_ANON_KEY;
const accessToken = process.env.SUPABASE_ACCESS_TOKEN;

if (!dbPassword || !serviceRoleKey || !anonKey) {
  console.error(`
Missing credentials. Set these environment variables:

  SUPABASE_DB_PASSWORD       — Database password from project creation
  SUPABASE_SERVICE_ROLE_KEY  — Settings → API → service_role (secret)
  SUPABASE_ANON_KEY          — Settings → API → anon public

Optional:
  SUPABASE_ACCESS_TOKEN      — Account → Access Tokens (auto-disable email confirm)

Project: ${SUPABASE_URL}
`);
  process.exit(1);
}

const encodedPassword = encodeURIComponent(dbPassword);
// Use IPv4 pooler (db.*.supabase.co is often IPv6-only and fails on some networks)
const POOLER_HOST = process.env.SUPABASE_POOLER_HOST ?? 'aws-0-ap-northeast-1.pooler.supabase.com';
const DIRECT_URL = `postgresql://postgres.${PROJECT_REF}:${encodedPassword}@${POOLER_HOST}:5432/postgres`;
const DATABASE_URL = `postgresql://postgres.${PROJECT_REF}:${encodedPassword}@${POOLER_HOST}:6543/postgres?pgbouncer=true`;

const serverEnv = `# Auto-generated by scripts/complete-supabase-setup.mjs
NODE_ENV=development
PORT=4000
API_PREFIX=api
APP_VERSION=0.1.0

DATABASE_URL=${DATABASE_URL}
DIRECT_URL=${DIRECT_URL}

SUPABASE_URL=${SUPABASE_URL}
SUPABASE_SERVICE_ROLE_KEY=${serviceRoleKey}
SUPABASE_STORAGE_BUCKET=product-images

RAZORPAY_KEY_ID=
RAZORPAY_KEY_SECRET=
GSTIN=GSTIN-PENDING
`;

const clientEnv = `# Auto-generated by scripts/complete-supabase-setup.mjs
VITE_SUPABASE_URL=${SUPABASE_URL}
VITE_SUPABASE_ANON_KEY=${anonKey}
`;

async function runStorageSql() {
  const sqlPath = join(ROOT, 'supabase', 'storage-policies.sql');
  const sql = readFileSync(sqlPath, 'utf8');
  const client = new pg.Client({ connectionString: DIRECT_URL, ssl: { rejectUnauthorized: false } });
  await client.connect();
  try {
    await client.query(sql);
    console.log('✓ Storage bucket + policies applied');
  } finally {
    await client.end();
  }
}

async function disableEmailConfirm() {
  if (!accessToken) {
    console.log('⚠ Skipping email confirm disable (set SUPABASE_ACCESS_TOKEN to automate)');
    console.log('  Manual: Authentication → Providers → Email → disable Confirm email');
    return;
  }
  const res = await fetch(`https://api.supabase.com/v1/projects/${PROJECT_REF}/config/auth`, {
    method: 'PATCH',
    headers: {
      Authorization: `Bearer ${accessToken}`,
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({ mailer_autoconfirm: true }),
  });
  if (!res.ok) {
    const text = await res.text();
    throw new Error(`Auth config update failed: ${res.status} ${text}`);
  }
  console.log('✓ Email confirmation disabled (mailer_autoconfirm)');
}

async function main() {
  console.log(`Setting up LeafyLand → ${SUPABASE_URL}\n`);

  writeFileSync(SERVER_ENV, serverEnv);
  writeFileSync(CLIENT_ENV, clientEnv);
  console.log('✓ Wrote server/.env and client/.env');

  await runStorageSql();

  try {
    await disableEmailConfirm();
  } catch (err) {
    console.warn('⚠', err.message);
  }

  console.log('\nPushing Prisma schema...');
  execSync('npx prisma db push', { cwd: join(ROOT, 'server'), stdio: 'inherit' });

  console.log('\nSeeding database...');
  execSync('npm run db:seed', { cwd: join(ROOT, 'server'), stdio: 'inherit' });

  console.log('\n✅ Supabase setup complete!');
  console.log('   Run: npm run dev');
  console.log('   Login: admin@leafyland.com / admin123');
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
